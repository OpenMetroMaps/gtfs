package org.openmetromaps.gtfs4j.cli;

import de.topobyte.utilities.apache.commons.cli.commands.ArgumentParser;
import de.topobyte.utilities.apache.commons.cli.commands.ExeRunner;
import de.topobyte.utilities.apache.commons.cli.commands.ExecutionData;
import de.topobyte.utilities.apache.commons.cli.commands.RunnerException;
import de.topobyte.utilities.apache.commons.cli.commands.options.DelegateExeOptions;
import de.topobyte.utilities.apache.commons.cli.commands.options.ExeOptions;
import de.topobyte.utilities.apache.commons.cli.commands.options.ExeOptionsFactory;

public class Gtfs4jCli
{

	public static ExeOptionsFactory OPTIONS_FACTORY = new ExeOptionsFactory() {

		@Override
		public ExeOptions createOptions()
		{
			DelegateExeOptions options = new DelegateExeOptions();
			options.addCommand("show-info", RunShowInfo.OPTIONS_FACTORY,
					RunShowInfo.class);
			options.addCommand("show-agencies", RunShowAgencies.OPTIONS_FACTORY,
					RunShowAgencies.class);
			options.addCommand("show-routes", RunShowRoutes.OPTIONS_FACTORY,
					RunShowRoutes.class);
			options.addCommand("show-stops", RunShowStops.OPTIONS_FACTORY,
					RunShowStops.class);
			options.addCommand("filter-routes", RunFilterRoutes.OPTIONS_FACTORY,
					RunFilterRoutes.class);
			return options;
		}

	};

	public static void main(String[] args) throws RunnerException
	{
		String name = "gtfs4j";

		ExeOptions options = OPTIONS_FACTORY.createOptions();
		ArgumentParser parser = new ArgumentParser(name, options);

		if (args.length == 0) {
			System.out.println("gtfs4j Command Line Interface");
			System.out.println();
			options.usage(name);
			System.exit(1);
		}

		ExecutionData data = parser.parse(args);
		if (data != null) {
			ExeRunner.run(data);
		}
	}

}
